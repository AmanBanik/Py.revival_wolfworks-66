#testing out Math Module
import math
x=3
y=5
w=3.897
z=-32.78
print(math.sqrt(x))  # Square root of x
print(math.pow(x, y))  # x raised to the power of y
print(math.ceil(w))  # Round w up to the nearest upper integer
print(math.floor(w))  # Round w down to the nearest lower integer
print(math.fabs(z))  # Absolute value of z
print(math.factorial(x))  # Factorial of x
print(math.gcd(x, y))  # Greatest common divisor of x and y
print(math.pi)  # Value of pi
print(math.e)  # Value of e
print(math.sin(math.pi / 2))  # Sine of pi/2
print(math.cos(0))  # Cosine of 0
print(math.tan(math.pi / 4))  # Tangent of pi/4
print(math.log(100, 10))  # Logarithm of 100 to base 10
print(math.log2(256))  # Logarithm of 256 to base 2
print(math.log(1024, 2))  # Logarithm of 1024 to base 2
print(math.degrees(math.pi))  # Convert radians to degrees
print(math.radians(180))  # Convert degrees to radians
print(math.trunc(w))  # Truncate w to an integer
print(math.isclose(0.1 + 0.2, 0.3))  # Check if two floating-point numbers are close
print(math.hypot(3, 4))  # Hypotenuse of a right triangle with sides 3 and 4
print(math.isfinite(1.0))  # Check if 1.0 is finite
